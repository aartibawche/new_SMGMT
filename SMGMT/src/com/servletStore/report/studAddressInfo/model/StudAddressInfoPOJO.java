package com.servletStore.report.studAddressInfo.model;

public class StudAddressInfoPOJO {
	int id;
	String stdDiv,division,trustyInfo,schoolName,address;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getStdDiv() {
		return stdDiv;
	}
	public void setStdDiv(String stdDiv) {
		this.stdDiv = stdDiv;
	}
	public String getDivision() {
		return division;
	}
	public void setDivision(String division) {
		this.division = division;
	}
	public String getTrustyInfo() {
		return trustyInfo;
	}
	public void setTrustyInfo(String trustyInfo) {
		this.trustyInfo = trustyInfo;
	}
	public String getSchoolName() {
		return schoolName;
	}
	public void setSchoolName(String schoolName) {
		this.schoolName = schoolName;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	
	

}
